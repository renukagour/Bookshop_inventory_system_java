    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();

        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this Book", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TableModel model = jTable2.getModel();
            String total = model.getValueAt(index, 3).toString();
            int Qty = Integer.parseInt(model.getValueAt(index, 2).toString());
            String title = model.getValueAt(index, 0).toString();
            NewQty = Qty + oldQty;

            updateQuantity(NewQty, bid);
            bookNameByCategory(category1);

            grandTotal = grandTotal - Integer.parseInt(total);
            labelGrandTotal.setText(String.valueOf(grandTotal));
            //update quantity need to write
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable2MouseClicked

