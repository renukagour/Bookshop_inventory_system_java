    public void validateFields() {
        String cname = txtCName.getText();
        String cmobilenumber = txtCMobileNumber.getText();
        String cemail = txtCEmail.getText();
        String caddress = txtcAddress.getText();
        if (!cname.equals("") && cmobilenumber.matches(mobileNumberPattern) && cmobilenumber.length() == 10 && cemail.matches(emailPattern) && grandTotal > 0 && !caddress.equals("")) {
            btnPrint.setEnabled(true);
        } else {
            btnPrint.setEnabled(false);
        }
    }

