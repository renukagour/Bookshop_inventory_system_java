    public void bookNameByCategory(String Category) {
        DefaultTableModel dtm = (DefaultTableModel) bookTable.getModel();
        dtm.setRowCount(0);
        ArrayList<Book> list = BookDao.gettAllRecordsByCategory(Category);
        Iterator<Book> it = list.iterator();
        while (it.hasNext()) {
            Book book = it.next();
            dtm.addRow(new Object[]{book.getBid(), book.getTitle(), book.getAuthor(), book.getPublisher(), book.getCategory(), book.getPrice(), book.getQuantity()});
        }

    }

    public void filterRecordsBytitle(String title, String Category) {
        DefaultTableModel dtm = (DefaultTableModel) bookTable.getModel();
        dtm.setRowCount(0);
        ArrayList<Book> list = BookDao.filterRecordsBytitle(title, Category);
        Iterator<Book> it = list.iterator();
        while (it.hasNext()) {
            Book book = it.next();
            dtm.addRow(new Object[]{book.getBid(), book.getTitle(), book.getAuthor(), book.getPublisher(), book.getCategory(), book.getPrice(), book.getQuantity()});
        }

    }

